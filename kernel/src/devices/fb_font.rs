//! 8x16 IBM VGA font for the linear framebuffer.
//!
//! [`FONT`] is a baked-in `[[u8; 16]; 128]` glyph table covering the 7-bit
//! ASCII range. Each entry is 16 rows of 8 pixels; the most-significant bit of
//! a row byte is the leftmost pixel, matching the convention used by
//! [`crate::devices::framebuffer`] and the [`gfx`](crate::devices::gfx)
//! primitives.
//!
//! # Source
//!
//! The bitmaps are the public-domain IBM VGA BIOS 8x16 font (the ROM
//! `FONT_8x16` table at `int 10h/AX=1124h`). It is the canonical PC console
//! font: every emulator and OS bootloader that ships an "8x16" font uses this
//! exact bitmap set, so on-screen glyphs match what a real VGA text mode
//! would render for the same character codes.
//!
//! # Encoding
//!
//! Rows are stored top-to-bottom. Within a row, bit 7 (0x80) is the leftmost
//! of the 8 pixel columns, bit 0 (0x01) the rightmost. To test whether pixel
//! column `x` (0..8) is set in row `y` (0..16) of glyph `c`:
//!
//! ```ignore
//! let on = (FONT[c as usize][y] >> (7 - x)) & 1 == 1;
//! ```
//!
//! Control codes (0x00..0x1F) are intentionally blank: the console layer
//! interprets `\n`, `\r`, `\t` etc. as cursor motion and never looks up their
//! glyphs, and drawing a visible box for a stray control byte would be more
//! confusing than drawing nothing. The 0x80..0xFF range is outside the 128
//! entries this table holds; callers must clamp or substitute before indexing.

/// Glyph geometry: every entry in [`FONT`] is this many rows tall.
pub const GLYPH_HEIGHT: usize = 16;

/// Glyph geometry: every row in a glyph encodes this many pixel columns.
pub const GLYPH_WIDTH: usize = 8;

