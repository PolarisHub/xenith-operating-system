/// Safe glyph lookup that never panics.
///
/// Returns the bitmap for `byte` when `byte < 128`, a blank glyph for control
/// codes (0x00..0x1F, which are already blank in the table), and a fallback
/// "outlined cell" glyph for bytes >= 128 so non-ASCII bytes render as a
/// visible placeholder rather than indexing out of bounds.
///
/// Callers that have already bounds-checked `byte < 128` and want the table
/// entry verbatim should index [`FONT`] directly; this function is the safe
/// default for paths that receive arbitrary `u8` (e.g. an untrusted string).
#[must_use]
pub fn glyph(byte: u8) -> [u8; GLYPH_HEIGHT] {
    if byte < 128 {
        FONT[byte as usize]
    } else {
        // Outlined cell: full top/bottom rows, left+right columns in the
        // middle. This matches the placeholder the framebuffer console used
        // before the real font landed, so high-bytes still read as a distinct
        // "character cell" per byte.
        let mut g = [0u8; GLYPH_HEIGHT];
        g[0] = 0xFF;
        g[GLYPH_HEIGHT - 1] = 0xFF;
        for row in &mut g[1..GLYPH_HEIGHT - 1] {
            *row = 0x81; // 0b10000001: left and right column pixels.
        }
        g
    }
}

/// Return `true` if pixel column `x` (0..8) of `row` is set in `glyph`.
///
/// `x` is taken modulo 8 so callers do not have to mask before testing; this
/// is cheap and keeps the lookup branchless for in-bounds `x`.
#[inline]
#[must_use]
pub fn pixel_on(glyph: &[u8; GLYPH_HEIGHT], row: usize, x: usize) -> bool {
    debug_assert!(row < GLYPH_HEIGHT, "row {row} out of glyph height");
    let bits = glyph[row.min(GLYPH_HEIGHT - 1)];
    let shift = 7 - (x & 7);
    (bits >> shift) & 1 == 1
}
